
package riyazihesablama;

public class RiyaziHesab {
    public static void main(String[] args) {
        
        double x=4;
        double y=5;
            System.out.println("x = "+x+" (Double Data Type)");
            System.out.println("y = "+y+" (Double Data Type)");
            System.out.println("---------------------------------------------");
        
            System.out.println("x ve y Deyerlerinin En Boyuk Olani: "+ Math.max(x, y));
            System.out.println("x ve y Deyerlerinden En Kicik Olani:" + Math.min(x, y)); 
            System.out.println("---------------------------------------------");
           
        double pi = Math.PI;
            System.out.println("'pi' Deyiseninin PI Deyeri: " +pi);    
            System.out.println("---------------------------------------------");
         
            System.out.println("'x' Deyerinin Kvardat Koku: " +Math.sqrt(x));
            System.out.println("---------------------------------------------------");
         
            System.out.println("'x'-i 'y' Deyeri Defe Quvvete Yukseltmesi: " +Math.pow(x, y) );
            
            
            System.out.println("Bizi Izlediyiniz Ucun Tewekkurler....");
            
    }
    
}
